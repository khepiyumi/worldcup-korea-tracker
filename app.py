# 3. 실시간 테이블 연산 프로세스 엔진
# ====================================================
def calculate_all_standings(fixtures):
    teams = {}
    for m in fixtures:
        g = m["조"]
        h, a = m["홈팀"], m["원정팀"]
        hs, as_ = m["홈스코어"], m["원정스코어"]
        
        for t in [h, a]:
            if t not in teams:
                teams[t] = {"국가": t, "조": g, "승점": 0, "경기수": 0, "득점": 0, "실점": 0}
                
        teams[h]["경기수"] += 1
        teams[a]["경기수"] += 1
        teams[h]["득점"] += hs
        teams[h]["실점"] += as_
        teams[a]["득점"] += as_
        teams[a]["실점"] += hs
        
        if hs > as_:
            teams[h]["승점"] += 3
        elif hs < as_:
            teams[a]["승점"] += 3
        else:
            teams[h]["승점"] += 1
            teams[a]["승점"] += 1
            
    df = pd.DataFrame(teams.values())
    df["골득실"] = df["득점"] - df["실점"]
    df["남은경기"] = 3 - df["경기수"]
    return df

# 기본 데이터 프레임 연산
