c2.metric("32강 확정 여부", f"⚠️ 와일드카드 경합 중 (12개팀 중 {wc_rank}위)")
c3.metric("현재 성적 (승점 / 골득실)", f"{int(korea_info['승점'])}점 / {int(korea_info['골득실'])}")

