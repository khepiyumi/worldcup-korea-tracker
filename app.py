# ====================================================
# 4. 입력 데이터를 기반으로 실시간 테이블 연산
# ====================================================
processed_list = []

for g_name, teams in base_data.items():
    for team in teams:
        t_name = team["국가"]
        g_data = {
            "조": g_name, "국가": t_name, "승점": team["승점"],
            "경기수": team["경기수"], "득점": team["득점"], "실점": team["실점"]
        }
        
        # 사용자가 화면에서 경기가 종료되었다고 체크한 경우 실시간 누적 연산 처리
        if t_name in modified_results:
            res = modified_results[t_name]
            g_data["경기수"] += 1
            g_data["득점"] += res["득점"]
            g_data["실점"] += res["실점"]
            if res["득점"] > res["실점"]:
                g_data["승점"] += 3
            elif res["득점"] == res["실점"]:
                g_data["승점"] += 1
                
        processed_list.append(g_data)

df_dynamic = pd.DataFrame(processed_list)
df_dynamic["골득실"] = df_dynamic["득점"] - df_dynamic["실점"]

ordered_groups = []
third_places = []

# 피파 정렬 규정 가동 (승점 -> 골득실 -> 다득점 순 정렬)
for g in sorted(df_dynamic["조"].unique()):
    g_df = df_dynamic[df_dynamic["조"] == g].copy()
    g_df = g_df.sort_values(["승점", "골득실", "득점"], ascending=False).reset_index(drop=True)
    g_df["조내순위"] = g_df.index + 1
    ordered_groups.append(g_df)
    if len(g_df) >= 3:
        third_places.append(g_df.iloc[2])

all_groups_df = pd.concat(ordered_groups).reset_index(drop=True)

# 와일드카드 실시간 변동 테이블 조립
wildcard_df = pd.DataFrame(third_places)
wildcard_df = wildcard_df.sort_values(["승점", "골득실", "득점"], ascending=False).reset_index(drop=True)
wildcard_df["와일드카드 순위"] = wildcard_df.index + 1
wildcard_df["32강 진출 여부"] = wildcard_df["와일드카드 순위"].apply(lambda x: "✅ 진출 성공" if x <= 8 else "❌ 탈락 위험")

# ====================================================
